 <section id="el-sitio">
<h1>El sitio</h1>
<p>
Este sitio web es mi ciberespacio poético personal. Aquí uso «poético» en dos sentidos: (1) el propósito principal de enfocarme en mi poesía, y (2) la etimología de la palabra, la cual deriva del griego ποίησις <i>póiēsis</i> ‘fabricación, creación, producción’, que a su vez proviene del infinitivo ποιεῖν <i >poiêin</i> ‘hacer’. Este segundo uso también tiene un sentido doble: se refiere a los escritos y al sitio web mismo, el cual elaboro «manualmente», desde sus elementos básicos de diseño (HTML y CSS). Lo desarrollo perpetuamente, de modo que continuará habiendo añadiduras, como proceso poiético-noético de <i>production désirante</i>.
</p>
<p>
Mediante el botón ubicado en la parte superior izquierda del sitio (es visible en todas las páginas de este) se accesa un menú que muestra títulos generales (poemarios, ensayos), para mediante esa lista navegar a los textos correspondientes. En caso que sea preferible para el lector, no es necesario un modo oscuro en el navegador de internet para leer aquí, pues acostumbro utilizar tipografía clara sobre fondos oscuros.
</p>
</section>

<section id="el-titulo">
<h1>El título</h1>
<p>
El título latino de este <i>locus</i> cibernético está construido según la declinación del caso genitivo plural de cada palabra, con la concordancia gramatical necesaria:
</p>
<ul>
    <li><i>Scrīptum</i> ‘una composición escrita, escrito, tratado, libro, obra’ (en latín clásico, un sustantivo neutro) > <i>scrīpt<b>ōrum</b></i> ‘<b>de</b> escrito<b>s</b>’</li> 
    <li><i>Poēticum</i> ‘poético’ (neutro) > <i>poētic<b>ōrum</b></i> ‘<b>de</b> poétic<b>os</b>’</li>
    <li><i>Alium</i> ‘otro’ (neutro) > <i>ali<b>ōrum</b></i> ‘<b>de</b> otr<b>os</b>’</li>
    <li><i>-que</i> ‘y’ como sufijo enclítico (a diferencia de <i>et</i>, el étimo común de dicha conjunción en las lenguas romances) > <i>aliōrum<b>que</b></i> ‘<b>y</b> de otros’</li> 
</ul>
<p>
Entonces el título completo, <i>Scrīptōrum poēticōrum aliōrumque</i>, significa «De escritos poéticos y otros».
</p>
</section>
  
<section id="los-escritos">
<h1>Los escritos</h1>
<p>
La lengua original es el español, desde el cual traduzco al inglés y al francés. En lo concerniente a posibles definiciones estrictas de «poesía», así como sobre si los textos en cuestión son o no poemas en prosa, tal indagación no es una prioridad a ser tratada aquí. Dejo esas consideraciones a discreción del lector. En mi perspectiva, es más valioso que la lectura sea estimulante, con una mecánica de oscilación entre lo reconocible y lo mistificante — y en primera instancia el gozo de escribir. En todo caso, continuaré usando la palabra según sea utilizable.
</p>
<p>
Entiendo mi poesía como un aspecto de un nexo cognitivo sintético que involucra mi pensamiento, mis sensaciones, etc. —en una palabra, mi espíritu—, desarrollado en una dinámica lingüística estimulada por afectos, por conceptos, y por mi memoria de ambos. La mayor parte de mi vida he tenido un interés fundamental en la filosofía, lo cual cultivo continuamente. Mi género literario favorito es la ciencia ficción (y disfruto ciertos tipos de fantasía). Naturalmente, mi escritura en general y mi poesía en particular se orientan filosóficamente y <i>ciencio-ficcionalmente</i> (aunque también hay algunos poemas que difieren de ello). Más allá de categorizaciones precisas, en cuanto a distinciones nítidas entre áreas temáticas, o de género, los bordes son más borrosos en algunos de mis textos poéticos que en otros.
</p>
<p>
Si la obra de arte es un compuesto de perceptos y de afectos, y si la poesía es un modo de arte lingüístico, entonces en la construcción de poemas se desea producir sensaciones <i>mediante</i> signos lingüísticos. Siendo estos últimos comunicados por escrito —en el caso mío y de este sitio web—, las grafías resultantes son, obviamente, los perceptos más elementales en el proceso. Entonces la interpretación que se hace de ellos es concomitante con la producción de afectos, los cuales son desarrollados —creados y experimentados— por el escritor y eventualmente experimentados por el lector. Deseo crear tales afectos, para mí y para otros, y que tales afectos creen deseo de búsquedas cognitivas — afectivas, conceptuales, mnemónicas, etc. Ese ciclo ocurre durante la escritura misma, cuestión radical en el placer de la creación (poética). Deseo que algo similar ocurra durante la lectura que otras personas hagan de los poemas, junto con un posible movimiento hipertextual vinculable con dicha actividad.
</p>
<p>
Si la filosofía es la producción de conceptos, y si la literatura de ciencia ficción, como obra narrativa con su construcción de mundo —y por lo tanto otro modo de arte lingüístico—, implica afectos narrativos, entonces escribo poesía impulsada por mis fascinaciones conceptuales y narrativas, una <i>puissance</i> que sintetiza ambas en textos que no son únicamente presentaciones artísticas de conceptos, ni únicamente relatos ficcionales. Junto con ello, me gusta que mis poemas se caractericen por una cierta rareza (muy interesantes la palabra inglesa <i>weird</i> y su etimología), con tonos oníricos, lúdicos, y libertad de fluidez — la fecunda influencia del surrealismo. Ea pues la coyuntura de mi escritura.
</p>
<p>
Aprecio la rima y otros modos de ritmo, como la prosodia acentual (la cual puede ser muy bella). Sin embargo, para mí, tales aspectos son secundarios para disfrutar de la poesía, en comparación con lo más fructífero de los matices semánticos. Un poeta comúnmente es un jugador semántico, así como sintáctico. Juega con símbolos y con afectos, manifiesta visiones. Es placentero para mí tener oportunidades de exploración, de descubrimiento, en los poemas u otros trabajos. He ahí esa búsqueda en sí como movimiento incesante (¿una <i>jouissance</i>?) de un deseo cognitivo constante.
</p>
</section>

<section id="citas-prolegomenicas">
<h1>Citas prolegoménicas</h1>
<p>
Me gusta desarrollar un archivo de extractos de textos varios, principalmente filosóficos, así como de algunas otras áreas inextricablemente conectadas en mi perspectiva filosófica. Es un hábito que se origina en una colección creciente de documentos digitales para referencia personal. En vista de la cantidad de fragmentos que quiero mostrar, decidí recopilarlos en <a href="https://excerptionum-philosophicarum.github.io">otro sitio web</a> dedicado a tal propósito. En cuanto a mi atracción por ciertos de esos temas en especial, se trata de algo que valoro como y que presento estéticamente como citas <i>prolegoménicas</i>, aunque no siempre son precursoras de lo que escribo: tales lecturas simultáneamente provienen de e influyen en mis intereses epistémicos. Por lo tanto, efectivamente, algunas de ellas enriquecen más directamente mis escritos.
</p>  
</section>
 
